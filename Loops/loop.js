// while loops and for loops

//while loop
var number = 1;

while(number <= 450){
    console.log(number);
    number++
}

// for loop
/* when using for loops its best to use let instead of var
because let is only used for a given code block and var means its 
globally used */

for(let num = 1; num<= 20; num++){
    console.log(num);
}

/* For loops are used when you know the range to loop where
 as While loops are used when you don't know the range to 
  loop */

  





